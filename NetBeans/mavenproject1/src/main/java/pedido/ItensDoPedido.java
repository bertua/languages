public class ItensDoPedido {

    private int idItensDoPedido;
    private int idPedido;
    private int idSabor;
    private int idTamanho;
    private double valor;
    
    public int getIdItensDoPedido() {
        return idItensDoPedido;
    }

    public void setIdItensDoPedido(int idItensDoPedido) {
        this.idItensDoPedido = idItensDoPedido;
    }

    public int getIdPedido() {
        return idPedido;
    }

    public void setIdPedido(int idPedido) {
        this.idPedido = idPedido;
    }

    public int getIdSabor() {
        return idSabor;
    }

    public void setIdSabor(int idSabor) {
        this.idSabor = idSabor;
    }

    public int getIdTamanho() {
        return idTamanho;
    }

    public void setIdTamanho(int idTamanho) {
        this.idTamanho = idTamanho;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }
    
}
