    public void salvar(Usuario usuario) {
        try (BufferedReader reader = new BufferedReader(new FileReader("usuarios.json"))) {
            usuarios = gson.fromJson(reader, new TypeToken<ArrayList<Usuario>>(){}.getType());
            if (usuarios == null) {
                usuarios = new ArrayList<>();
            }    
        } catch (IOException e) {
        } 
        
        usuarios.add(usuario);
        try (FileWriter writer = new FileWriter("usuarios.json")) {
            gson.toJson(usuarios, writer);
            JOptionPane.showMessageDialog(this, "Cadastro salvo!");
        } catch (IOException e) {
        }
    }
    
    public boolean verificar(Usuario usuario){
        boolean bool = true;
        try (BufferedReader reader = new BufferedReader(new FileReader("usuarios.json"))) {
            usuarios = gson.fromJson(reader, new TypeToken<ArrayList<Usuario>>(){}.getType());
            if (usuarios == null) {
                usuarios = new ArrayList<>();
            }
            for(Usuario i: usuarios){
                if(i.getUsuario().equals(usuario.getUsuario()) || i.getEmail().equals(usuario.getEmail())){
                    bool = false;
                }
            }
        } catch (IOException e) {
        }
        return bool;
    }

