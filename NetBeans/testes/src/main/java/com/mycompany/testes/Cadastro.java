    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        // TODO add your handling code here:
        boolean error = false;
        if(tfUsuario.getText().isEmpty()){
            error = true;
            JOptionPane.showMessageDialog(rootPane, "Usuario inválido");
        }
        if(tfNome.getText().isEmpty() || !tfNome.getText().matches("^[a-zA-Z]+( [a-zA-Z]+)?$")){
            error = true;
            JOptionPane.showMessageDialog(rootPane, "Nome inválido");
        }
        if(!tfEmail.getText().matches("^[\\w-\\.]+@[\\w-]+\\.[a-zA-Z]{2,}$") || tfEmail.getText().length() > 200){
            error = true;
            JOptionPane.showMessageDialog(rootPane, "email inválido");
        }
        
        if(tfSenha.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Preencha a senha");
            error = true;
        }
        if(tfSenha.getText().length() < 6 && !tfSenha.getText().matches("^(?=.*[a-z])(?=.*[A-Z])(?=.*\\d)(?=.*[!@#$%^&*()_+\\-=\\[\\]{};':\"\\\\|,.<>\\/?]).+$")){
            JOptionPane.showMessageDialog(rootPane, "senha deve ter no minimo, uma letra minuscula, uma maiuscula, um número e um caracter especial");
            error = true;
        }
        
        if(error == false){
            Usuario usuario = new Usuario();
            usuario.setUsuario(tfUsuario.getText());
            usuario.setNome(tfNome.getText());
            usuario.setEmail(tfEmail.getText());
            usuario.setSenha(tfSenha.getText());
            if(verificar(usuario)){
                salvar(usuario);
                tfUsuario.setText("");
                tfNome.setText("");
                tfEmail.setText("");
                tfSenha.setText("");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Usuario ou email já em uso");
            }
        }
       
        
    }//GEN-LAST:event_btCadastrarActionPerformed

